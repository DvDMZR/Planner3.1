const makeT = (lang) => {
    const dict = TRANSLATIONS[lang] || TRANSLATIONS['de'];
    return (key, vars) => {
        let str = dict[key];
        if (str === undefined) str = TRANSLATIONS['de'][key];
        if (str === undefined) str = key;
        if (vars) {
            Object.entries(vars).forEach(([k, v]) => {
                str = str.replace(new RegExp('\\{' + k + '\\}', 'g'), v);
            });
        }
        return str;
    };
};
